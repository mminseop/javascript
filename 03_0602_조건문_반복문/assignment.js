// 4. 별 찍기 (도전과제)

/**
 * JavaScript 3일차 과제
 * 별 찍기
 *
 * 출력 확인법
 * Terminal 창에 다음 코드를 작성해주세요.
 * node main
 *
 * 요구사항
 * - 반복문을 활용하여 콘솔에 별을 출력해주세요.
 * - 변수 n에 맞춰 출력해주세요.
 */

let n = 5;
console.log('==============');
let i = 5;
console.log("공백 수:", n - i);    // 1
console.log("별 수:", 2 * i - 1);  // 

// 여기에 작성해주세요.
for (let i = 1; i <= n; i++) {
    let star = "";
    let space = "";

    for (let k = 1; k <= n-i; k++) {
        space += " ";
    }
    for (let j=1; j <= 2*i-1; j++) {
        star += "*";
    }
    console.log(space, star);
}

function star(num) {
  for (let i=0; i < num; i++) {
    let str = '';
    // 공백
    for (let j=-0; j< num-i-1; j++) {
      str += ' ';
    }
    // 왼쪽 삼각형
    for (let j=0; j<i+1; j++) {
      str += '*';
    }
    //  오른쪽 삼각형
    for (let j=0; j<i; j++) {
      str += '*';
    }
    console.log(str);
  }
}
star(5);
